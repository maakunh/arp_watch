    {
        //既存履歴の削除（ブロックしないデータを削除）
            $del_date = new Carbon(); //X分前のデータ
            Arp_record::where('status', 2)->where('created_at','<', $del_date->subMinutes(30))->delete();
            Arp_block::where('created_at','<', $del_date->subMinutes(1440))->delete();
            return response("OK_DEL".PHP_EOL, 201);
     }

